    function AverageOfSet(Vector){
        var sum = 0;

        for(var i=0; i<Vector.length; i++)
            sum += Vector[i];

        return sum/Vector.length;
    }

    function MaxOfSet(Vector){
        var max = 0, value = 0;

        for(var i=0; i<Vector.length; i++){
            value = Vector[i];

            if( value >= max ) 
                max = value;
        }
        
        return max;
    }

    function MinOfSet(Vector){
        var min = MaxOfSet(Vector), value = 0;

        for(var i=0; i<Vector.length; i++){
            value = Vector[i];

            if( value <= min )
                min = value;
        }
        
        return min;
    }

